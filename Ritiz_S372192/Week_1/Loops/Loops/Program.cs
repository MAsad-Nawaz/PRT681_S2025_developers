using System;

class Program
{
    static void Main(string[] args)
    {
        // For Loop
        Console.WriteLine("For Loop:");
        for (int i = 1; i <= 5; i++)
        {
            Console.WriteLine(i);
        }

        // While Loop
        Console.WriteLine("While Loop:");
        int j = 1;
        while (j <= 5)
        {
            Console.WriteLine(j);
            j++;
        }
    }
}
